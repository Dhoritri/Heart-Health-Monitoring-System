function showHospitals() {
    const city = document.getElementById('city').value;
    const hospitalContainer = document.getElementById('hospitals');
    hospitalContainer.innerHTML = '';

    if (city && hospitals[city]) {
        hospitals[city].forEach(hospital => {
            const hospitalCard = document.createElement('div');
            hospitalCard.className = 'hospital-card';

            const hospitalImage = document.createElement('img');
            hospitalImage.src = hospital.img;
            hospitalImage.alt = hospital.name;

            const hospitalName = document.createElement('h3');
            hospitalName.textContent = hospital.name;

            const hospitalAddress = document.createElement('p');
            hospitalAddress.textContent = hospital.address;

            const websiteButton = document.createElement('button');
            websiteButton.textContent = 'Visit Website';
            websiteButton.className = 'hospital_button'; 
            websiteButton.addEventListener('click', () => {
                window.open(hospital.website, '_blank');
            });

            hospitalCard.appendChild(hospitalImage);
            hospitalCard.appendChild(hospitalName);
            hospitalCard.appendChild(hospitalAddress);
            hospitalCard.appendChild(websiteButton);
            

            hospitalContainer.appendChild(hospitalCard);
        });
    }
}

